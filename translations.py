def local_crop_name(lang_code, crop_key):
    """Returns the crop name in the selected language; falls back to the
    English (dataset) label, title-cased, if no translation is available."""
    info = CROP_INFO.get(crop_key.lower())
    if not info:
        return crop_key.title()
    if lang_code == "en":
        return crop_key.title()
    return info.get(lang_code, crop_key.title())


