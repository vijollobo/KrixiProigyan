def local_number(lang_code, number):
    """Converts the digit characters in `number` (any int/float/str) to the
    native script of `lang_code`, leaving other characters (., -, %, etc.)
    untouched. Falls back to Western digits if no script is registered."""
    for languages, numerals in NUMERAL_GROUPS:
        if lang_code in languages:
            return "".join(
                numerals[ord(ch) - ord("0")] if "0" <= ch <= "9" else ch
                for ch in str(number)
            )
    return str(number)


def format_number(lang_code, value, decimals=2):
    """Rounds `value` to `decimals` places and renders it in the native
    numeral script of `lang_code`. Use this for any numeric value shown
    in the UI (confidence %, soil health score, NPK readings, etc.)."""
    rounded = round(float(value), decimals)
    text = f"{rounded:.{decimals}f}"
    return local_number(lang_code, text)
